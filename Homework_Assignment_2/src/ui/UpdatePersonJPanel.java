    public void populateTable(){
        dtm.setRowCount(0);
        for(Person personU: personDirectory.getPersonList()){
            Object[] row = new Object[4];
            row[0] = personU;
            row[1] = personU.getName();
            row[2] = personU.getEmail();
            row[3] = personU.getCourseName();
            
            dtm.addRow(row);
        }
    }
    
