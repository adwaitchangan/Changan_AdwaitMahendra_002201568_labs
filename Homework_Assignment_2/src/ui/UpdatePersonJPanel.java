    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText().trim();
        String email = txtEmail.getText().trim();
        String phoneNumber = txtPhoneNo.getText().trim();
        String coursesName = txtCourseName.getText().trim();
        String nuid = txtNUID.getText().trim();
        String address = txtAddress.getText().trim();

        if (name.isEmpty() || email.isEmpty() || phoneNumber.isEmpty() || coursesName.isEmpty() || nuid.isEmpty() || address.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please make sure all fields are filled in before proceeding.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        person.setName(name);
        person.setEmail(email);
        person.setContactInfo(phoneNumber);
        person.setCourseName(coursesName);
        person.setNeuID(nuid);
        person.setAddress(address);
        
        if(person.isAccountCreatedStatus()==true){
            for(Person user : userDirectory.getUserList()){
                user.setName(name);
                user.setEmail(email);
                user.setContactInfo(phoneNumber);
                user.setCourseName(coursesName);
                user.setNeuID(nuid);
                user.setAddress(address);
            }
        }

        JOptionPane.showMessageDialog(this, "Person Information Updated.");

        MainPanel.remove(this);
        CardLayout layout = (CardLayout) MainPanel.getLayout();
        layout.previous(MainPanel);
        populateTable();
        
        
    }//GEN-LAST:event_btnSaveActionPerformed

